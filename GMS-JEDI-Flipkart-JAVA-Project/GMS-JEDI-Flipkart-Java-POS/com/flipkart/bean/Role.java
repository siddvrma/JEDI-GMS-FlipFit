/**
 * Enumerates the roles in the system.
 */
public enum Role {
    /**
     * Represents the role of an administrator.
     */
    ADMIN,

    /**
     * Represents the role of a gym owner.
     */
    GYMOWNER,

    /**
     * Represents the role of a customer.
     */
    CUSTOMER
}